a = prompt("Enter the number for day in the week: ");
if (a == 1) {
  console.log("Monday");
} else if (a == 2) {
  console.log("Thuesday");
} else if (a == 3) {
  console.log("Wednesday");
} else if (a == 4) {
  console.log("Thursday");
} else if (a == 5) {
  console.log("Friday");
} else if (a == 6) {
  console.log("Saturday");
} else if (a == 7) {
  console.log("Sunday");
} else {
  console.log("There are only 7 days in a week");
}
